defmodule Mix.Tasks.Translate do
  @moduledoc """
  Adds translations to PO files for German, Italian, Spanish, and French.
  Run with: mix translate
  """
  use Mix.Task

  @shortdoc "Add translations to PO files"

  def run(_args) do
    languages = ["de", "it", "es", "fr"]

    Enum.each(languages, fn lang ->
      path = "priv/gettext/#{lang}/LC_MESSAGES/default.po"
      Mix.shell().info("Processing #{path}...")

      content = File.read!(path)
      translations = get_translations(lang)

      updated = add_translations_to_content(content, translations)
      File.write!(path, updated)

      Mix.shell().info("  Updated #{map_size(translations)} translations")
    end)

    Mix.shell().info("\nDone! Run 'mix compile --force' to compile translations.")
  end

  defp add_translations_to_content(content, translations) do
    Enum.reduce(translations, content, fn {msgid, msgstr}, acc ->
      escaped_msgid = Regex.escape(msgid)
      pattern = ~r/(msgid "#{escaped_msgid}"\nmsgstr )("")/
      replacement = "\\1\"#{escape_po(msgstr)}\""
      Regex.replace(pattern, acc, replacement)
    end)
  end

  defp escape_po(str) do
    str
    |> String.replace("\\", "\\\\")
    |> String.replace("\"", "\\\"")
  end

